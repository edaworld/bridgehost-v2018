/*********************************************************************************************************
*
*   模块名称 : BSP模块(For STM32F1XX)
*   文件名称 : bsp.c
*   版    本 : V1.0
*   说    明 : 这是硬件底层驱动程序模块的主文件。主要提供 bsp_Init()函数供主程序调用。
               主程序的每个c文件可以在开头添加 #include "bsp.h" 来包含所有的外设驱动模块。
*
*********************************************************************************************************/
#include "bsp.h"
#define USBToPCPort COM1
/**********************************************************************************************************
*   函 数 名: bsp_Init
*   功能说明: 初始化硬件设备。只需要调用一次。该函数配置CPU寄存器和外设的寄存器并初始化一些全局变量。
*            全局变量。
*   形    参：无
*   返 回 值: 无
**********************************************************************************************************/
void bsp_Init(void)
{
	/* 优先级分组设置*/
	NVIC_PriorityGroupConfig(NVIC_PriorityGroup_1); //初始化中断优先级

	/*初始化LED*/
	LED_Init(); //初始化Led的GPIO

	/*初始化定时器*/
	SysTickTimer_Init();    //初始化系统滴答定时器 (此函数会开中断)
	bsp_HardTimerInit();    //初始化硬件定时器
    
	/*以下为开启3个串口*/
	UART_InitALL(); //初始化串口

	/*初始化SX1278*/
	RFGPIOInit();
	RFInit();    //射频模块初始化
	RFRxMode();  //进入接收模式

}

/*********************************************************************************************************
*   函 数 名: bsp_RunPer10ms
*   功能说明: 该函数每隔10ms被Systick中断调用1次。详见 bsp_timer.c的定时中断服务程序。一些处理时间要求不严格的
*           任务可以放在此函数。比如：按键扫描、蜂鸣器鸣叫控制等。
*********************************************************************************************************/
void bsp_RunPer10ms(void)
{
	static uint32_t s_count1 = 0;
	if(++s_count1 == 50)
	{
		s_count1 = 0;
		LED2_TOGGLE();
	}
}

